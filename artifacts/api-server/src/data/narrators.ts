// Index for fast lookup
const NAME_INDEX = new Map<string, NarratorEntry>();
const ID_INDEX = new Map<string, NarratorEntry>();

function stripTashkeel(text: string): string {
  return text.replace(/[\u064B-\u065F\u0670\u06D6-\u06DC\u06DF-\u06E4\u06E7\u06E8\u06EA-\u06ED]/g, "").trim();
}

for (const narrator of NARRATOR_DB) {
  ID_INDEX.set(narrator.id, narrator);
  for (const norm of narrator.nameNormalized) {
    NAME_INDEX.set(stripTashkeel(norm), narrator);
  }
}

// Arabic name particles that must NOT count toward meaningful word matches
const ARABIC_PARTICLES = new Set([
  "بن", "بنت", "ابن", "ابنة", "أبو", "أبي", "أبا", "أم", "عن", "من",
  "بن", "عبد", "الله", "بن", "ال", "و", "في", "عن",
]);

function meaningfulWords(words: string[]): string[] {
  return words.filter((w) => w.length >= 3 && !ARABIC_PARTICLES.has(w));
}

export function findNarratorByName(name: string): NarratorEntry | undefined {
  const stripped = stripTashkeel(name.trim());
  if (!stripped) return undefined;

  // 1. Exact match
  if (NAME_INDEX.has(stripped)) return NAME_INDEX.get(stripped);

  // 2. Normalized name is fully contained in the extracted text (key ⊆ extracted)
  //    Only allow keys that are long enough to be specific (≥ 10 chars, ≥ 2 meaningful words)
  const strippedWords = stripped.split(/\s+/);
  const strippedMeaningful = new Set(meaningfulWords(strippedWords));

  let bestMatch: NarratorEntry | undefined;
  let bestScore = 0;

  for (const [key, narrator] of NAME_INDEX) {
    // Skip very short keys — too generic
    if (key.length < 8) continue;

    const keyWords = key.split(/\s+/);
    const keyMeaningful = meaningfulWords(keyWords);

    // Must have at least 2 meaningful words in the key
    if (keyMeaningful.length < 2) continue;

    // Count meaningful key words that appear in the extracted name
    const matchCount = keyMeaningful.filter((w) => strippedMeaningful.has(w)).length;

    // Require ALL meaningful key words to match (precision over recall)
    if (matchCount === keyMeaningful.length && matchCount >= 2) {
      const score = matchCount;
      if (score > bestScore) {
        bestScore = score;
        bestMatch = narrator;
      }
    }
  }

  return bestMatch;
}

export function findNarratorById(id: string): NarratorEntry | undefined {
  return ID_INDEX.get(id);
}

